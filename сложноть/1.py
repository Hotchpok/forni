# Функция для поиска минимального и максимального элементов в массиве
def find_min_max(arr):
    # Проверка на пустой массив
    if not arr:
        return None, None  # Возвращаем None для пустого массива
    
    # Инициализируем min_val и max_val первым элементом массива
    min_val = max_val = arr[0]  # Инициализация первым элементом
    
    # Проходим по всем элементам массива, начиная со второго (индекс 1)
    for num in arr[1:]:
        # Если текущий элемент меньше текущего минимума
        if num < min_val:
            min_val = num  # Обновляем минимум
        # Иначе если текущий элемент больше текущего максимума
        elif num > max_val:
            max_val = num  # Обновляем максимум
    
    # Возвращаем кортеж (min_val, max_val)
    return min_val, max_val

# Пример использования
array = [3, 1, 4, 1, 5, 9, 2, 6, 5, 3, 5]
# Вызываем функцию и получаем минимальный и максимальный элементы
min_element, max_element = find_min_max(array)
# Выводим результаты
print(f"Минимальный элемент: {min_element}, Максимальный элемент: {max_element}")