# Задаем нижний предел интегрирования
a = 2
# Задаем верхний предел интегрирования
b = 6

# Задаем количество прямоугольников для метода численного интегрирования
# Чем больше n, тем точнее результат
n = 1000

# Вычисляем ширину каждого прямоугольника (шаг интегрирования)
dx = (b - a) / n

# Инициализируем переменную для накопления площади  
area = 0

# Цикл по всем прямоугольникам (от 0 до n-1)
for i in range(n):
    # Вычисляем x-координату левого края текущего прямоугольника
    x = a + i * dx
    # Добавляем площадь текущего прямоугольника (высота * ширина) к общей площади
    area += (x**2) * dx

# Выводим результат с пояснением
# Форматированная строка показывает границы интегрирования и вычисленную площадь
print(f"Площадь под графиком функции на отрезке [{a}; {b}] равна: {area}")
